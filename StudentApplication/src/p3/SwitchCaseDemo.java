package p3;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		
		int x = 1;
		
		switch (x) {
		case 20:
			System.out.println(" its 20");
			break;
			
		case 2:
			System.out.println(" its 2");
			break;
		case 1:
			System.out.println(" its 1");
			
		case 10:
			System.out.println(" its 10");
			break;
			
		
		}
		
	}
}
